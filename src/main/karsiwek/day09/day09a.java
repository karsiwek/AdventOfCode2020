    static Long checkRule(final List<Long> input, final Integer length) {
        for(int i=length;i<input.size();i++){
            Long number = input.get(i);
            boolean foundsum = false;
            for(int j=i-length;j<i;j++){
                for(int k = j+1; k<i;k++){
                    foundsum = foundsum | (number == input.get(j) + input.get(k));
                }
            }
            if(!foundsum) {
                return input.get(i);
            }
        }
        return 0l;
    }


