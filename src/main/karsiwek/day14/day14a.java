    static Long parseInput(final String input) {
        List<String> program = Arrays.asList(input.split("\n"));
        Map<Integer, Long> memory = new HashMap<>();
        String mask = "";
        for(String line : program) {
            if(line.split(" = ")[0].equals("mask")){
                mask = line.split(" = ")[1];
            } else {
                int addr = Integer.parseInt(line.substring(line.indexOf("[")+1, line.indexOf("]")));
                long value = Long.parseLong(line.substring(line.indexOf("=")+2));

                long valueAfterMask = getValueAfterMask(value, mask);
                memory.put(addr, valueAfterMask);
            }
        }

    return  memory.values().stream().reduce(0l, (a,b) -> a+b);


    }

    private static long getValueAfterMask(long value, String mask) {
        long carreer = 1;

        for(int i=mask.length()-1;i>=0;i--) {
            if(mask.substring(i, i+1).equals("1")) {
                value = value | carreer;
            } else if(mask.substring(i, i+1).equals("0")) {
                if((value & carreer) > 0){
                    value -= carreer;
                }
            }
            carreer = carreer *2;
        }

        return value;
    }

